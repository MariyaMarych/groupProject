package Loops;

public class LoopsHomework {
    public static void main(String[] args) {

        int i,j,k,l;
        for(i=1; i<7;i++){
            System.out.println();

            for(j=1;j<=1;j++){
                System.out.print("******");
                System.out.println();

            }
            for(k=1; k<=2;k++){
                System.out.println("*    *");
                System.out.println();

            }
            for(l=1;l<=1;l++){
                System.out.print("******");
                break;
            }


        }

    }
}





