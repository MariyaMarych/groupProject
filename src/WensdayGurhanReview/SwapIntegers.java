package WensdayGurhanReview;

public class SwapIntegers {
    public static void main(String[] args) {












        int a =5;
        int b = 6;

        a =a+b; // a=11
        b= a-b; //b=5
        a =a-b; // a-b = 11- 5 =6
        System.out.println(a);
        System.out.println(b);

        int v = 7;
        int k = 3;

        int temp =0;

        temp=v;
        v=k;
        k = temp;

        System.out.println(v);
        System.out.println(k);




    }


}
